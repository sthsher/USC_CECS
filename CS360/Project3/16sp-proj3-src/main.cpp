void Simulate(Simulator* sim1, Project3 & p3)
{
	// Start simulation
	int steps = 0;
    int waitCounter = 100; // amount to wait between steps (milliseconds)
	sim1->reset();
	
    while (!sim1->robotFoundTarget() && !sim1->isRobotDead()) {  // loop until your robot find the target or dies
        RobotAction a = p3.getOptimalAction(sim1->getRobotLocation());

        // call the simulator to move your robot and count the steps
        sim1->moveRobot(a);
		steps++;
	
        sim1->display();
        		
        #if defined(_WIN32) || defined(_WIN64)
        Sleep(waitCounter);
        #else	
        //struct timespec req, rem;
        //req.tv_nsec = 1000000*waitCounter;
        //req.tv_sec = 0;
        //nanosleep(&req, &rem);
		usleep(1000*waitCounter);
        #endif
    }
    
	if (!sim1->isRobotDead()) 
		cout<<"Wheelbot found the target in "<<steps<<" steps !!!"<<endl;
    else 
		cout<<"Wheelbot stepped on a Lego at "<<sim1->getCrashLocation()<<" and died after "<<steps<<" steps ..."<<endl;
	
	cout<<"Press \'enter\' to restart simulation. Press \'q\' followed by \'enter\' to quit."<<endl;
	
}

